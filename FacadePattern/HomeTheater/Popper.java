package DesignPatterns.FacadePattern.HomeTheater;

public class Popper {
    public void on() {
        System.out.println("Popper turned on");
    }

    public void off() {
        System.out.println("Popper turned off");
    }

    public void pop() {
        System.out.println("Popping the popcorn");
    }
}
